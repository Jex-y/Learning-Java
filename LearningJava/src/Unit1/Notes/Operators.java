package Unit1.Notes;

public class Operators {
    public static void main(String[] args) {
        /*
         *   ----------------------------------------------------------------------------------------------------
         *                                         Arithmetic NotesUnit1.Operators
         *  ----------------------------------------------------------------------------------------------------
         */

        // Arithmetic operators are used to perform basic mathematical operations.


        int int1 = 1;
        int int2 = 2;
        int result;

        result = int1 + int2;
        result = int1 - int2;
        result = int1 * int2;
        result = int1 / int2;
        result = int1 % int2;
        result ++;
        result --;

        /*
         *   ----------------------------------------------------------------------------------------------------
         *                                         Assignment NotesUnit1.Operators
         *  ----------------------------------------------------------------------------------------------------
         */

        // Assignment operators can modify a variable whilst assigning it.

        result = int1;
        result += int1;
        result -= int1;
        result *= int1;
        result /= int1;
        result %= int1;
        // Bitwise
        result &= int1;
        result |= int1;
        result ^= int1;
        result >>= int1;
        result <<= int1;


        /*
         *   ----------------------------------------------------------------------------------------------------
         *                                         Comparison NotesUnit1.Operators
         *  ----------------------------------------------------------------------------------------------------
         */

        // Used to compare two values.

        boolean bool;

        bool = (int1 == int2);
        bool = (int1 != int2);
        bool = (int1 > int2);
        bool = (int1 < int2);
        bool = (int1 >= int2);
        bool = (int1 >= int2);


        /*
         *   ----------------------------------------------------------------------------------------------------
         *                                         Logical NotesUnit1.Operators
         *  ----------------------------------------------------------------------------------------------------
         */

        bool = (true && false);
        bool = (true || false);
        bool = !true;









    }
}
